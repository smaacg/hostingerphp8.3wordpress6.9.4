      $join_error = '';

      if ( isset( $_POST['join_submit'] ) ) {

        /* Honeypot */
        if ( ! empty( $_POST['join_website'] ) ) {
          $join_error = '安全驗證失敗，請重新送出。';
        }

        /* Nonce */
        elseif ( ! wp_verify_nonce( $_POST['_wpnonce_join'] ?? '', 'weixiaoacg_join' ) ) {
          $join_error = '安全驗證失敗，請重新整理頁面後再試。';
        }

        else {
          $f_name     = sanitize_text_field( $_POST['join_name']    ?? '' );
          $f_email    = sanitize_email(      $_POST['join_email']   ?? '' );
          $f_type     = sanitize_text_field( $_POST['join_type']    ?? '' );
          $f_anime    = sanitize_textarea_field( $_POST['join_anime']  ?? '' );
          $f_intro    = sanitize_textarea_field( $_POST['join_intro']  ?? '' );
          $f_discord  = sanitize_text_field( $_POST['join_discord'] ?? '' );

          $allowed_types = [ 'editor', 'writer', 'both' ];

          if ( empty( $f_name ) ) {
            $join_error = '請填寫您的姓名／暱稱。';
          } elseif ( ! is_email( $f_email ) ) {
            $join_error = '請輸入有效的 Email 地址。';
          } elseif ( strtolower( $f_email ) === 'weixiaoacg.com@gmail.com' ) {
            $join_error = '請輸入您自己的 Email 地址。';
          } elseif ( ! in_array( $f_type, $allowed_types, true ) ) {
            $join_error = '請選擇申請方向。';
          } elseif ( mb_strlen( $f_intro ) < 10 ) {
            $join_error = '自我介紹至少需要 10 個字。';
          } else {

            /* ── IP 頻率限制（每小時 3 次）── */
            $ip       = sanitize_text_field( $_SERVER['REMOTE_ADDR'] ?? '' );
            $tk       = 'join_limit_' . md5( $ip );
            $count    = (int) get_transient( $tk );
            if ( $count >= 3 ) {
              $join_error = '您的申請次數過多，請一小時後再試。';
            } else {
              set_transient( $tk, $count + 1, HOUR_IN_SECONDS );

              $type_map = [
                'editor' => '資料編輯 / 內容維護',
                'writer' => '投稿作者 / 新聞撰稿',
                'both'   => '兩者皆有興趣',
              ];
              $type_label = $type_map[ $f_type ] ?? $f_type;

              /* 寄信給站長 */
              $subject = "[微笑動漫] 加入申請：{$f_name}（{$type_label}）";
              $body    = "【加入申請通知】\n\n"
                . "姓名／暱稱：{$f_name}\n"
                . "Email：{$f_email}\n"
                . "申請方向：{$type_label}\n"
                . ( $f_discord ? "Discord ID：{$f_discord}\n" : '' )
                . "\n【喜歡的動漫作品】\n{$f_anime}\n"
                . "\n【自我介紹】\n{$f_intro}\n"
                . "\n送出時間：" . current_time( 'Y-m-d H:i:s' ) . "\n"
                . "IP：{$ip}\n";

              $headers = [
                'Content-Type: text/plain; charset=UTF-8',
                'From: 微笑動漫申請系統 <weixiaoacg.com@gmail.com>',
                'Reply-To: ' . $f_name . ' <' . $f_email . '>',
              ];

              $sent = wp_mail( 'weixiaoacg.com@gmail.com', $subject, $body, $headers );

              if ( $sent ) {
                /* 自動回覆給申請者 */
                $auto_subject = '【微笑動漫】已收到您的加入申請';
                $auto_body    = "您好，{$f_name}！\n\n"
                  . "感謝您申請加入微笑動漫編輯團隊 🎉\n\n"
                  . "我們已收到您的申請（{$type_label}），\n"
                  . "將於 3～5 個工作天內以此 Email 與您聯繫，請耐心等候。\n\n"
                  . "━━━━━━━━━━━━━━━━━━━━━━━\n"
                  . "申請方向：{$type_label}\n"
                  . "送出時間：" . current_time( 'Y-m-d H:i:s' ) . "\n"
                  . "━━━━━━━━━━━━━━━━━━━━━━━\n\n"
                  . "※ 此為系統自動發送，請勿直接回覆此信。\n\n"
                  . "— 微笑動漫 團隊\n" . home_url() . "\n";

                wp_mail( $f_email, $auto_subject, $auto_body, [
                  'Content-Type: text/plain; charset=UTF-8',
                  'From: 微笑動漫 <weixiaoacg.com@gmail.com>',
                ] );

                $join_sent = true;
              } else {
                $join_error = '寄送失敗，請稍後再試或直接寄信至 weixiaoacg.com@gmail.com。';
